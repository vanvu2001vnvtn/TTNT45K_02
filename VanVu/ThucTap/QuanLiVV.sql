CREATE DATABASE TRANVANVU
GO
USE TRANVANVU
GO
CREATE TABLE DANGNHAP(
TaiKhoan varchar(50) primary key,
MatKhau varchar(50) not null)
go
CREATE TABLE KHACHHANG(
MaKH varchar(10) primary key,
TenKH nvarchar(50) not null,
DiaChi nvarchar(100),
GioiTinh bit,
NgaySinh Date,
ThanThiet int)
GO
CREATE TABLE HOADON(
MaHD INT PRIMARY KEY,
MaKH VARCHAR(10) FOREIGN KEY REFERENCES KHACHHANG ON DELETE CASCADE ON UPDATE CASCADE,
NgayDat DATE,
TongTien INT)
GO
CREATE TABLE HD_CHITIET(
MaHD INT FOREIGN KEY REFERENCES HOADON ON DELETE CASCADE ON UPDATE CASCADE,
TenSP NVARCHAR(100),
NoiDung NVARCHAR(500),
NgayHenTra DATE,
NgayTra DATE,
ThanhTien INT)
GO
